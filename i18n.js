// Comme t(), mais pour une langue EXPLICITE, indépendante de la langue de
// l'interface. Utilisé pour les documents (diplômes, bulletins) où
// l'enseignante choisit la langue du document séparément de la langue de
// son propre écran — sans ce helper, ces textes suivaient par erreur la
// langue de l'interface au lieu de la langue choisie pour le document.
function tLang(lang, key, ...args) {
  const dict = I18N[lang] || I18N.fr;
  const entry = dict[key] ?? I18N.fr[key] ?? key;
  return typeof entry === "function" ? entry(...args) : entry;
}
